//Arithmetic in Rust

fn main() {
    let mut num1:i32=50;
    let mut num2:i32=5;
    let mut num3:i32=0;

    //Add
    num3=num1+num2;
    println!("Sum is: {}",num3);

    //Sub
    num3=num1-num2;
    println!("Diff is: {}",num3);

    //Product
    num3=num1*num2;
    println!("Product is: {}",num3);

    //Divide
    num3=num1/num2;
    println!("Dividing Num1 and Num2: {}",num3);

}
