// Rust program to print the ASCII value of RUST

fn main() {
    let mut ch:char='R';
    println!("ASCII value: {}",ch as u32);
    
    ch='U';
    println!("ASCII value: {}",ch as u32);

    ch='S';
    println!("ASCII value: {}",ch as u32);   
    
    ch='T';
    println!("ASCII value: {}",ch as u32);    
}


